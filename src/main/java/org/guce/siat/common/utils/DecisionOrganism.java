package org.guce.siat.common.utils;



/**
 * The Class FileField.
 */
public class DecisionOrganism
{

	/** The code. */
	private String code;

	/** The libelle. */
	private String libelle;

	/** The file type. */
	private String observation;

	/**
	 * Gets the code.
	 *
	 * @return the code
	 */
	public String getCode()
	{
		return code;
	}

	/**
	 * Sets the code.
	 *
	 * @param code
	 *           the new code
	 */
	public void setCode(final String code)
	{
		this.code = code;
	}

	/**
	 * Gets the libelle.
	 *
	 * @return the libelle
	 */
	public String getLibelle()
	{
		return libelle;
	}

	/**
	 * Sets the libelle.
	 *
	 * @param libelle
	 *           the new libelle
	 */
	public void setLibelle(final String libelle)
	{
		this.libelle = libelle;
	}

	/**
	 * Gets the observation.
	 *
	 * @return the observation
	 */
	public String getObservation()
	{
		return observation;
	}

	/**
	 * Sets the observation.
	 *
	 * @param observation
	 *           the new observation
	 */
	public void setObservation(final String observation)
	{
		this.observation = observation;
	}

}
